number = int(input("Enter an integer: "))

if number % 2 == 0:
    print(f"{number} is even.")
else:
    print(f"{number} is odd.")



# If the number is divisible by 2 (remainder is 0), it is even.
# If the number is not divisible by 2 (remainder is not 0), it is odd.

